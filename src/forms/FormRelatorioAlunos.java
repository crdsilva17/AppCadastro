     private void preencheTabela()
    {
        //Criar uma var "alunoDAO" 
        //pegar dados dos alunos na lista e jogar na tabela
        AlunoDAO alunoDAO = new AlunoDAO();
        List<Aluno> listaAlunos = alunoDAO.getAlunos();
        
        DefaultTableModel tabelaAlunos = (DefaultTableModel) tblAlunos.getModel();
        //limpar tabela para preenchimento
        tabelaAlunos.setNumRows(0);
        //percorre o listaAlunos e inserir na tabelaAlunos
       for(Aluno a : listaAlunos){
           Object[] obj = new Object[]{
               a.getId(),
               a.getNomealuno(),
               a.getCursoid()
           };
           tabelaAlunos.addRow(obj);
       }
    }

